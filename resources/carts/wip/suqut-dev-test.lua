-- Used to add entites to the inventory
function add(initial_value,add_value)
  local temp_value=initial_value[add_value] or 0 
  initial_value[add_value]=temp_value+1
end

-- Used to subtract entites to the inventory
function subtract(initial_value,subtract_value)
  local temp_value=initial_value[subtract_value] or 0 
  initial_value[subtract_value]=temp_value-1
end

function drawInventory()
  if menu.subSt == "INVENTORY" then
    menuBox(30, 68, 48, 112, 4, 9, 1, 9, 1)
    print("Hotbar", 13, 17)
    
    if keyp(controls.up) then menu.select = menu.select - 1 sfx(3, "3-F") end
    if keyp(controls.down) then menu.select = menu.select + 1 sfx(3, "3-F") end
    if keyp(controls.left) then menu.select = menu.select - 6 sfx(3, "3-F") end
    if keyp(controls.right) then menu.select = menu.select + 6 sfx(3, "3-F") end
    inv.hotbar.sel = menu.select + 1
    
    local slots = 11
    
    if menu.select < 0 then menu.select = slots+(menu.select+1)
    elseif menu.select > slots then menu.select = (menu.select-1-slots)
    end
    
    
    local currentSlot = 0
        
    for j = 0, 1, 1 do
      if currentSlot > slots then
        break
      end
      
      for q = 0, 5, 1 do
        if menu.select == currentSlot then
          menuBox(22+j*16, 33+q*16, 12, 12, 4, 1, 9, 1, 9)
        else
          menuBox(22+j*16, 33+q*16, 12, 12, 4, 9, 1, 9, 1)
        end
        if inv.hotbar[currentSlot+1] ~= nil then
          rect((22-5)+j*16, (33-5)+q*16, 10, 10, 0)
          spr(inv.hotbar[currentSlot+1], (22-4)+j*16, (33-4)+q*16)
        end
        currentSlot = currentSlot + 1
      end
    end
  end
end

-- Toggles the inventory display on/off via button press
function toggle_inventory()
  if keyp(controls.inventory) and menu.subSt == "NONE" then
    menu.subSt = "INVENTORY"
  elseif keyp(controls.inventory) and menu.subSt == "INVENTORY" then
    menu.subSt = "NONE"
  end
end


-- How the player selects which block to place
function player_inventory()
    local hud_y=127

    -- Enter inventory mode
    if btnp(controls.x) then
        if inventory_mode then
            inventory_mode=false
        else
            inventory_mode=true
        end
    end 

    if inventory_mode then
        -- Select previous block
        if btnp(controls.up) then
            selected_block=selected_block-1
        end
        -- Select next block
        if btnp(controls.down) then
            selected_block=selected_block+1
        end
    end

    -- Render the inventory
    for blocks,count in pairs(inventory) do
        -- Add more background as the number expands
        -- to improve readability of numbers
        if count==0 then
            -- Render nothing
        elseif count<=10 then
            spr(blocks,2,hud_y)
            rect(10,hud_y,8,8,0)
            print(count,12,hud_y+2,15)
            hud_y=hud_y-10
        elseif count>10 and count<=100 then
            spr(blocks,2,hud_y)
            rect(10,hud_y,8,8,0)
            rect(18,hud_y,8,8,0)
            print(count,12,hud_y+2,15)
            hud_y=hud_y-10
        elseif count>100 and count<1000 then -- Item cap is 999
            spr(blocks,2,hud_y)
            rect(10,hud_y,8,8,0)
            rect(18,hud_y,8,8,0)
            rect(26,hud_y,8,8,0)
            print(count,12,hud_y+2,15)
            hud_y=hud_y-10
        end
    end
    
    -- Old inventory
    rect(230,0,240,10,0)
    spr(selected_block,231,1,0)
end

